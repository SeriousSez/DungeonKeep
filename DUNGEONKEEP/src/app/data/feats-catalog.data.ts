export type FeatCategory = 'origin' | 'general' | 'epic-boon';

export interface FeatFeature {
    title: string;
    text: string;
}

export interface FeatEntry {
    name: string;
    slug: string;
    category: FeatCategory;
    prerequisite?: string;
    benefit: string;
    features?: FeatFeature[];
    abilityScoreIncrease?: string;
    source: string;
    pageRef?: string;
}

export function getFeatBySlug(slug: string): FeatEntry | undefined {
    return featCatalogEntries.find((f) => f.slug === slug);
}

